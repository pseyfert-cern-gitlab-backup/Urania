g2_10.SetLineColor(kRed)
g2_11.SetLineColor(kRed)

for gr in [g1_00,g1_01,g1_10,g1_11,g2_00,g2_01,g2_10,g2_11]:
   gr.GetXaxis().SetTitle("t_{true} (ps)")
   gr.GetYaxis().SetTitle("Arbitrary units")

g1_00.SetTitle("2011, L0Global TIS")
g1_01.SetTitle("2011, L0Hadron TOS")
